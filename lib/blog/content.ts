export function getBlogPostsByCategory(category: string): BlogPost[] {
  return BLOG_POSTS.filter((post) => post.category === category).sort(
    (a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
  );
}

// Get related posts
export function getRelatedPosts(post: BlogPost, limit: number = 3): BlogPost[] {
  const related = post.relatedSlugs
    ?.map((slug) => getBlogPostBySlug(slug))
    .filter((p): p is BlogPost => p !== undefined)
    .slice(0, limit) || [];

  // If not enough related posts, fill with same category
  if (related.length < limit) {
    const sameCategoryPosts = BLOG_POSTS.filter(
      (p) => p.category === post.category && p.slug !== post.slug && !related.find((r) => r.slug === p.slug)
    ).slice(0, limit - related.length);
    related.push(...sameCategoryPosts);
  }

  return related;
}
